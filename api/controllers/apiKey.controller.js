const express = require('express')
const router = express.Router()
const ApiKeyService = require('../services/apiKey.service')
const { fetchLoggedInUser } = require('../api_util/api_util')
const logger = require('../../util/logger')

router.post('/cohorts/:cohortId/api-keys', fetchLoggedInUser, async (req, res) => {
  try {
    const user = req.session.user
    const { cohortId } = req.params
    const { name, expiresAt } = req.body

    if (!name || name.trim().length === 0) {
      return res.status(400).json({
        success: false,
        error: 'API key name is required',
      })
    }

    const apiKey = await ApiKeyService.createApiKey(user, parseInt(cohortId), name.trim(), expiresAt)

    logger.info(`API key created for cohort ${cohortId} by user ${user.id}`)

    res.json({
      success: true,
      data: apiKey,
    })
  } catch (error) {
    logger.error('Error creating API key:', error)
    res.status(error.message.includes('Unauthorized') ? 403 : 400).json({
      success: false,
      error: error.message,
    })
  }
})

